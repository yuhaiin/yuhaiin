package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"sort"
	"strconv"
	"strings"
)

type route struct {
	Method    string
	Pattern   string
	Operation string
}

func main() {
	input := flag.String("input", "", "v2_routes.go input path")
	output := flag.String("output", "", "generated TypeScript output path")
	flag.Parse()
	if *input == "" || *output == "" {
		fatalf("-input and -output are required")
	}
	routes, err := parseRoutes(*input)
	if err != nil {
		fatalf("parse routes: %v", err)
	}
	if err := os.WriteFile(*output, render(routes), 0o644); err != nil {
		fatalf("write generated client: %v", err)
	}
}

func parseRoutes(path string) ([]route, error) {
	file, err := parser.ParseFile(token.NewFileSet(), path, nil, 0)
	if err != nil {
		return nil, err
	}
	endpoints := make(map[string]string)
	var table *ast.CompositeLit
	for _, declaration := range file.Decls {
		gen, ok := declaration.(*ast.GenDecl)
		if !ok {
			continue
		}
		for _, specification := range gen.Specs {
			value, ok := specification.(*ast.ValueSpec)
			if !ok {
				continue
			}
			if gen.Tok == token.CONST {
				for index, name := range value.Names {
					if index < len(value.Values) {
						if literal, ok := value.Values[index].(*ast.BasicLit); ok && literal.Kind == token.STRING {
							endpoints[name.Name] = mustUnquote(literal.Value)
						}
					}
				}
			}
			if gen.Tok == token.VAR && len(value.Names) == 1 && value.Names[0].Name == "v2Routes" {
				table, _ = value.Values[0].(*ast.CompositeLit)
			}
		}
	}
	if table == nil {
		return nil, fmt.Errorf("v2Routes table not found")
	}

	routes := make([]route, 0, len(table.Elts))
	for _, element := range table.Elts {
		entry, ok := element.(*ast.CompositeLit)
		if !ok || len(entry.Elts) != 2 {
			return nil, fmt.Errorf("unexpected route entry")
		}
		endpoint, ok := entry.Elts[0].(*ast.Ident)
		if !ok {
			return nil, fmt.Errorf("route endpoint is not an identifier")
		}
		pattern, ok := entry.Elts[1].(*ast.BasicLit)
		if !ok || pattern.Kind != token.STRING {
			return nil, fmt.Errorf("route pattern is not a string")
		}
		method, path, ok := strings.Cut(mustUnquote(pattern.Value), " ")
		if !ok {
			return nil, fmt.Errorf("invalid route pattern %q", pattern.Value)
		}
		operation, ok := endpoints[endpoint.Name]
		if !ok {
			return nil, fmt.Errorf("endpoint %q has no value", endpoint.Name)
		}
		routes = append(routes, route{Method: method, Pattern: path, Operation: operation})
	}
	return routes, nil
}

func render(routes []route) []byte {
	operations := make(map[string]struct{}, len(routes))
	for _, route := range routes {
		operations[route.Operation] = struct{}{}
	}
	operationList := make([]string, 0, len(operations))
	for operation := range operations {
		operationList = append(operationList, operation)
	}
	sort.Strings(operationList)

	var output bytes.Buffer
	output.WriteString("// Code generated by cmd/httpapi-client-gen; DO NOT EDIT.\n\n")
	output.WriteString("export type RPCOperation =\n")
	for _, operation := range operationList {
		fmt.Fprintf(&output, "  | %s\n", strconv.Quote(operation))
	}
	output.WriteString(";\n\n")
	output.WriteString("type LegacyRoute = { method: string; pattern: string; operation: RPCOperation };\n\n")
	output.WriteString("const legacyRoutes: LegacyRoute[] = [\n")
	for _, route := range routes {
		fmt.Fprintf(&output, "  { method: %s, pattern: %s, operation: %s },\n", strconv.Quote(route.Method), strconv.Quote(route.Pattern), strconv.Quote(route.Operation))
	}
	output.WriteString("];\n\n")
	output.WriteString("export function rpcPath(operation: RPCOperation): string {\n  return `/api/v2/rpc/${operation}`;\n}\n\n")
	output.WriteString(`export function resolveRPCRoute(method: string, path: string): { operation: RPCOperation; params: Record<string, string> } {
  const actual = path.split("/").filter(Boolean);
  for (const route of legacyRoutes) {
    if (route.method !== method) continue;
    const expected = route.pattern.split("/").filter(Boolean);
    if (expected.length !== actual.length) continue;
    const params: Record<string, string> = {};
    let matched = true;
    for (let index = 0; index < expected.length; index++) {
      const part = expected[index];
      if (part.startsWith("{") && part.endsWith("}")) {
        params[part.slice(1, -1)] = decodeURIComponent(actual[index]);
      } else if (part !== actual[index]) {
        matched = false;
        break;
      }
    }
    if (matched) return { operation: route.operation, params };
  }
	  throw new Error("Unknown API route: " + method + " " + path);
}
`)
	return output.Bytes()
}

func mustUnquote(value string) string {
	result, err := strconv.Unquote(value)
	if err != nil {
		panic(err)
	}
	return result
}

func fatalf(format string, args ...any) {
	fmt.Fprintf(os.Stderr, format+"\n", args...)
	os.Exit(1)
}
